package Tp1;

public class Sommet {
    String nom;
    String couleur;

    public Sommet(String nom) {
        this.nom = nom;
        this.couleur = couleur;
    }
    public Sommet(String nom, String couleur) {
        this.nom = nom;
        this.couleur = couleur;
    }
    public void changerCouleur(String couleur){
        this.couleur = couleur;
    }

}
